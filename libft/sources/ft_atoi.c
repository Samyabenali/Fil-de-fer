#include "printf.h"

int		atoi2(const char *s, int i, int a, intmax_t b)
{
	while (s[i] >= 48 && s[i] <= 57)
	{
		b = b * 10 + s[i] - 48;
		i++;
	}
	if ((b > 2147483647 && a == 1) || (b * a) < -2147483648)
		return (-999999);
	if ((s[i] < 48 || s[i] > 57) && s[i] != ' ' &&
			s[i] != '\t' && s[i] != '\n' && s[i] != '\0')
		return (-999999);
	return (a * b);
}

int		ft_atoi(const char *s)
{
	int			i;
	int			a;
	intmax_t	b;

	i = 0;
	a = 1;
	b = 0;
	while (s[i] == '\n' || s[i] == '\b' || s[i] == '\t' ||
			s[i] == '\v' || s[i] == '\f' || s[i] == '\r' || s[i] == ' ')
		i++;
	if (s[i] == '-')
		a = -1;
	if (s[i] == '+' || s[i] == '-')
		i++;
	if ((s[i] < 48 || s[i] > 57) && s[i] != ' ' &&
			s[i] != '\t' && s[i] != '\n' && s[i] != '\0')
		return (-999999);
	else
		return (atoi2(s, i, a, b));
}
